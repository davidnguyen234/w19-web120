 <h2 class="pageID">Flexbox</h2>
 <br>
 <p>Before the Flexbox Layout module, there were four layout modes:</p><br>
 <ul id="flexbox-table">
  <li>Block, for sections in a webpage</li>
  <li>Inline, for text</li>
  <li>Table, for two-dimensional table data</li>
  <li>Positioned, for explicit position of an element</li>
</ul><br>

<p>The Flexible Box Layout Module, makes it easier to design flexible responsive layout structure without using float or positioning” (w3schools). This means more effective ways to align and distribute space for items within a container.</p><br>

<p>Flexbox allows containers to alter its item’s width and height to best fill the space. When using flexbox you need to think in terms of two axes, the main axis and the cross axis. The main axis is defined by the flex direction property, and the cross axis runs perpendicular to it.</p><br>

<p>The area of your website using flexbox is called a flex container. To create the flex container, you need to set the value of the area’s container’s display property to flex or incline-flex. By doing so, all direct children of this element become flex items. 
</p><br>


 <h2 class="pageID">Shopping Carts</h2>
 <br>

<p>The way a shopping cart works on a website is by linking multiple pages together through programming such as JavaScript that allows data to be transferred along with you as you move from page to page.</p><br>

<p>For example, when I go on Amazon’s website and search for shoes. I am directed to a page where I can add a pair of shoes to the shopping cart. After adding the pair of shoes, I am able to search for other items such as shirts. This will redirect me to a different page where I can add a shirt to the shopping cart which will have the transferred data of my shoes as well. 
</p><br>

<p>Monetizing websites simply refers to making money from your visitors. Monetization is measured by the volume of traffic your website gets and the average revenue from each visitor.
</p><br>

<p>There are a couple ways to incorporate a shopping car to your website. You use another company’s shopping cart such as PayPal. You can take advantage of open source software to get a free shopping cart you can add to your site. Lastly, you can code your own shopping cart.
</p><br>
